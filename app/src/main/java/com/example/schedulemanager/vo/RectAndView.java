package com.example.schedulemanager.vo;

import android.graphics.Rect;
import android.view.View;

public class RectAndView {
    private Rect rect;
    private View view;

    public Rect getRect() {
        return rect;
    }

    public void setRect(Rect rect) {
        this.rect = rect;
    }

    public View getView() {
        return view;
    }

    public void setView(View view) {
        this.view = view;
    }
}
